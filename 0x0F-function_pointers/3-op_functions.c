#include "3-calc.h"
/**
  * op_add - add things
  * @a: int
  * @b: int
  * Return: yes
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - sub things
  * @a: int
  * @b: int
  * Return: yes
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - mul things
  * @a: int
  * @b: int
  * Return: yes
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - div things
  * @a: int
  * @b: int
  * Return: yes
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - mod things
  * @a: int
  * @b: int
  * Return: yes
  */
int op_mod(int a, int b)
{
	return (a % b);
}
